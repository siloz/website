		<b>Why Are There Two Silo Types, and What Is the Difference?</b>
		<p>Silos break down into two main categories or types: <strong>personal</strong>, and <strong>community</strong>.</p>
		<p>Community silos require the silo administrator to be functioning in <strong>some official capacity</strong>, as a school representative, church administrator or minister, youth sports league commissioner, etc.  Community silos are appropriate whenever a collection of people is joined in the following ways:</p>
		<p>1. By membership in a club, team, organization, league, class, group (example: youth soccer league) and/or</p>
		<p>2. By association through a shared investment or shared beneficiary status (example: community playground), and/or</p>
		<p>3. By association through living in the close proximity (example: neighborhood cleanup project), and</p>
		<p>4. The central way the group is defined is <strong>not as family</strong> and <strong>open to the public</strong>. (Note: family reunions are always personal silos)</p>
		<p><strong> Everything else is a personal silo.</strong></p>
		<p>The idea behind segregating <strong>personal </strong>and <strong>community </strong>silos is a security feature: you essentially have to be invited to a personal silo, whereas community silos are visible on the site, and open for anyone to join.</p>
		<p>A stranger should not be able to pledge an item to benefit a person's grandmother's funeral expenses.  That invites unwanted associations and fraud.</p>
